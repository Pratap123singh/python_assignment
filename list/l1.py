l1=[1,2,13,-9,121,33,-25,5,8]
l2=[e for e in l1 if e > 3]
print(l2)

_11 = [e for e in l1 if e % 11 == 0]
print(_11)

l3 = [e if e > 3 else None for e in l1 ]
print(l3)

print("---------------------------insert---------------------------")
l2=[10,20,30,40,50]
print(l2)
l2.insert(3,-50)
print(l2)

print("---------------------------update---------------------------")
l2[3]=-60
print(l2)

print("---------------------------remove---------------------------")
l2.remove(-60)
print(l2)

print("---------------------------slicing---------------------------")
l4=[10,20,30,40,50,60]
print(f"l4[2:4]: {l4[2:4]}")
print(f"l4[-5:-1]: {l4[-5:-1]}")
print(f"l4[3:5:2]: {l4[3:5:2]}")
print(f"l4[0:5:2]: {l4[0:5:2]}")


print("---------------------------shallow copy---------------------------")
l5=[10,20,30,40]
print(l5)
l6=l5[::]
print(l6)
l5.remove(30)
print(l5)
print(l6)


